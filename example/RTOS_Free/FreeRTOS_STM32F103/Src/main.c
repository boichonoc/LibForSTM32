/* USER CODE BEGIN 4 */
void PrintCounter(uint8_t ID_Task)
{
  uint8_t i=0;
  if (myBinarySem01Handle != NULL)
    {
      if(osSemaphoreWait(myBinarySem01Handle , 100) == osOK)
        {  
          i++;
          for(i=1;i<=50;i++)
          {
            if(ID_Task==1)
            {
              sprintf(str1,"%d ",i);
              LCD_Goto(&hi2c1,50,2);
              OLED_string(&hi2c1, str1); 
            }
            else if(ID_Task==2)
            {
              sprintf(str1,"%d ",i);
              LCD_Goto(&hi2c1,50,4);
              OLED_string(&hi2c1, str1);
            }
            osDelay(500);
          }
          if(ID_Task==1)
          {
            LCD_Goto(&hi2c1,50,2);
            OLED_string(&hi2c1,"  ");
            ncount1=0;
          }
          else if(ID_Task==2)
          {
            LCD_Goto(&hi2c1,50,4);
            OLED_string(&hi2c1,"  ");
            ncount2=0;
          }
     osSemaphoreRelease(myBinarySem01Handle);
    }
  }
}
