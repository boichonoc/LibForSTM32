/* StartDefaultTask function */
void StartDefaultTask(void const * argument)
{

  /* USER CODE BEGIN 5 */
  tasks_started++;
  /* Infinite loop */
  for(;;)
  {
    ncount1++;
    sprintf(str1,"%lu ",ncount1);
    LCD_Goto(&hi2c1,75,2);
    OLED_string(&hi2c1,str1);
    PrintCounter(1);
    osDelay(1);
  }
  /* USER CODE END 5 */ 
}

